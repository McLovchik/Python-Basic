import re

list_example = 'А578ВЕ777 ОР233787 К901МН666 СТ46599 СНИ2929П777 666АМР666'

private_cars = re.findall(r'\b[АВЕКМНОРСТУХ]\d{3}[АВЕКМНОРСТУХ]+\d{2,3}\b', list_example)
print('Список номеров частных автомобилей:', private_cars)

taxi = re.findall(r'\b[АВЕКМНОРСТУХ]{2}\d{5,6}', list_example)
print('Список номеров такси:', taxi)

# Список номеров частных автомобилей: ['А578ВЕ777', 'К901МН666']
# Список номеров такси: ['ОР233787', 'СТ46599']
